ORG 0000H

MAIN:
    ;--------------------------------------------------
    ; Build A = 0x67 (First and primary build)
    ;--------------------------------------------------
    CLR A
    ORL A, #40H     ; 01000000
    ORL A, #20H     ; 01100000
    ORL A, #04H     ; 01100100
    ORL A, #02H     ; 01100110
    ORL A, #01H     ; 01100111 = 0x67
    
    ; A = 0x67
    
    ;--------------------------------------------------
    ; Build B = 0x09
    ;--------------------------------------------------
    MOV B, A        ; Temporarily save A in B
    CLR A
    ORL A, #08H     ; 00001000
    ORL A, #01H     ; 00001001 = 0x09
    XCH A, B        ; Swap: A=0x67, B=0x09
    
    ; A = 0x67, B = 0x09
    
    ;--------------------------------------------------
    ; Build R0 = 0x24
    ;--------------------------------------------------
    MOV R3, A       ; Save A in R3 first! R3 = 0x67
    CLR A
    ORL A, #20H     ; 00100000
    ORL A, #04H     ; 00100100 = 0x24
    MOV R0, A       ; R0 = 0x24
    MOV A, R3       ; Restore A = 0x67
    
    ; A = 0x67, B = 0x09, R0 = 0x24, R3 = 0x67
    
    ;--------------------------------------------------
    ; Build R1 = 0x07
    ;--------------------------------------------------
    MOV R3, A       ; Save A in R3 again (still 0x67)
    CLR A
    ORL A, #07H     ; 00000111 = 0x07
    MOV R1, A       ; R1 = 0x07
    MOV A, R3       ; Restore A = 0x67
    
    ; Final: A=0x67, B=0x09, R0=0x24, R1=0x07, R3=0x67
    
HOLD:
    SJMP HOLD

END